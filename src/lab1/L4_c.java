package lab1;

public class L4_c {
    public static void main(String[] args) {
        for(int i=0;i<=6;i++)
        {
            for(int j=1;j<i;j++)
            {
                System.out.print(j);
            }
            System.out.println("");
        }
        for(int i=5;i>0;i--)
        {
            for(int j=1;j<i;j++)
            {
                System.out.print(j);
            }
            System.out.println("");
        }
    }
}
